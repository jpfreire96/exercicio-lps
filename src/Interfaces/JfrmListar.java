    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ArrayList<Matricula> matriculas = new ArrayList<Matricula>();
        int lenght = jTable2.getRowCount();
        for (int i = 0; i < lenght; i++){
            Matricula temp= new Matricula();
            temp.setNumMatricula(Integer.parseInt(jTable2.getValueAt(i, 0).toString()));
            temp.setDataMatricula(jTable2.getValueAt(i, 1).toString());
            temp.setAluno(escola.findAluno(jTable2.getValueAt(i, 2).toString()));
            temp.setCurso(escola.findCurso(jTable2.getValueAt(i, 3).toString()));
            matriculas.add(temp);
        }
        escola.setMatriculasData(matriculas);
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

