    public JfrmListar() {
        initComponents();
        ArrayList<Matricula> matriculas = escola.getMatriculas();
        String[] col = {"Numero", "Data", "Aluno", "Curso"};
        DefaultTableModel tableModel;
        tableModel = new DefaultTableModel(col, 0);
        int lenght = matriculas.size();
        for (int i = 0; i < lenght; i++) {
            String[] temp = {String.valueOf(matriculas.get(i).getNumMatricula()), matriculas.get(i).getDataMatricula(), matriculas.get(i).getAluno().getNome(), matriculas.get(i).getCurso().getNome()};
            tableModel.addRow(temp);
        }
        jTable2.setModel(tableModel);
        this.setLocationRelativeTo(null);
        this.setResizable(false);
    }

