package Classes;

/**
 *
 * @author jpfreire
 */
public class Aluno {
    private int     cod;
    private String  nome;
    private String  cpf;
    
    public Aluno(int cod, String nome, String cpf){
        this.cod = cod;
        this.nome = nome;
        this.cpf = cpf;
    }
    
        public Aluno(){
        this.cod =-1;
        this.nome = "";
        this.cpf = "";
    }

    /**
     * @return the cod
     */
    public int getCod() {
        return cod;
    }

    /**
     * @param cod the cod to set
     */
    public void setCod(int cod) {
        this.cod = cod;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the cpf
     */
    public String getCpf() {
        return cpf;
    }

    /**
     * @param cpf the cpf to set
     */
    public void setCpf(String cpf) {
        this.cpf = cpf;
    }
}
